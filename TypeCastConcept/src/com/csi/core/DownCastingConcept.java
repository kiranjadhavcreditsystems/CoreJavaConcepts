package com.csi.core;

public class DownCastingConcept {
	public static void main(String[] args) {

		//Down Casting-> Higher to Lower[ex. double to int]
		
		
		double productPrice=5689.92;
		
		int pPrice= (int) productPrice;// DownCasting
		
		System.out.println("\n Product Price: "+ pPrice);
		
		
	}
}
